package polymorphism;

public class LgTV {
	
	public void turnOn() {
		System.out.println("Lg TV --- 전원켠다.");
	}
	public void turnOff() {
		System.out.println("Lg TV --- 전원끈다.");
	}
	public void soundUp() {
		System.out.println("Lg TV --- 소리 올린다.");
	}
	public void soundDown() {
		System.out.println("Lg TV --- 소리 내린다.");
	}
}
